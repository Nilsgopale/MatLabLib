plt.scatter([0.3, 3.8, 1.2, 2.5], [11, 25, 9, 26], color='darkgreen', marker='^')
plt.xlim(0.5, 4.5)
plt.show()
fig = plt.figure()
ax = fig.add_subplot(111)
ax.scatter(np.linspace(0, 1, 5), np.linspace(0, 5, 5))
plt.show()
